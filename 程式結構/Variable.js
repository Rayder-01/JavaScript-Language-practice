let pi = 3.14159;

// 以上程式碼的意義是：使用 let 語法定義一個名稱是 pi  
// 的變數，並將 3.14159 存入 pi 這個變數所對應的記憶體
// 的位置中。 

console.log(pi);
console.log(pi);
console.log(3.14159);  // 請將 __ 換成程式碼

