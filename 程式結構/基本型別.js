// String:

console.log('abc');             // 單引號
console.log("JavaScript");      // 雙引號
console.log("John's computer"); // 單雙混用
console.log("Peter's car");     // 單雙混用
console.log('It\'s ok!');       // escape character
console.log();

// Number:

console.log(-3);   // 整數
console.log(3.14); // 小數
console.log(2.5);  // 小數
console.log();

// Boolean:

console.log(true);
console.log(false);
console.log();

// Null: 代表有值，而它的值是 null（空值）

let a = null;
console.log(a);

// Undefined: 定義了一個變數，卻沒有存入它的值

let x;
console.log(x);  
let y;
console.log(y);

// Symbol: 可以先忽略