// 可以使用 $ 與 _ 
let $pi = 3.14;
let _pi = 3.14159;

console.log($pi);
console.log(_pi);

// 大小寫有區分
let num1 = 1;
let Num1 = 2;
// 是兩個不同的變數

console.log(num1);
console.log(Num1);

// 組合字使用 camelCase 命名（第一個字母小寫，
// 之後的字的第一個字母大寫）

let aNumber = 3;
let myAge = 18;

console.log(aNumber);
console.log(myAge);

